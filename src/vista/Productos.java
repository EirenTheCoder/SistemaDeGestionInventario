    private void listarProducto(){
        List<Producto> lista=daoPd.Listar();
        modeloProducto=(DefaultTableModel) tblProductos.getModel();
        Object[] ob=new Object[5];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getIdProducto();
            ob[1]=lista.get(i).getNomProd();
            ob[2]=lista.get(i).getStock();
            ob[3]=lista.get(i).getIdCategoria();
            ob[4]=lista.get(i).getPrecioVenta();
            modeloProducto.addRow(ob);
        }
        tblProductos.setModel(modeloProducto);
    }
    
    void numProducto(){
        int numero=daoPd.numeroProducto();
        if(numero==0){
            txtidProd.setText("1");
        }else{
           int i=numero;
           i=i+1;
           txtidProd.setText(i+"");
        }
    }
