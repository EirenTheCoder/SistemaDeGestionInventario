    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        // TODO add your handling code here:
        int fila=tblProductos.getSelectedRow();
        txtidProd.setText(tblProductos.getValueAt(fila, 0).toString());
        txtnombrePd.setText(tblProductos.getValueAt(fila, 1).toString());
        txtstock.setText(tblProductos.getValueAt(fila, 2).toString());
        txtidCategoria.setText(tblProductos.getValueAt(fila, 3).toString());
        txtprecioVenta.setText(tblProductos.getValueAt(fila, 4).toString());
        ct.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
        if(daoCt.buscar(ct)){
            txtNomCategoria.setText(ct.getNomCategoria());
        }
    }//GEN-LAST:event_tblProductosMouseClicked

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        pd.setIdProducto(Integer.parseInt(txtidProd.getText()));
        pd.setNomProd(txtnombrePd.getText());
        pd.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
        pd.setPrecioVenta(Double.parseDouble(txtprecioVenta.getText()));
        if(daoPd.insertar(pd)){
            MenuPrincipal mp=new MenuPrincipal();
            mp.Exito("El registro de producto se registro con éxito");
            limpiarCampos();
            limpiarTablaProducto();
            listarProducto();
            numProducto();
        } else {
            MenuPrincipal mp1=new MenuPrincipal();
            mp1.Error("El regitro de producto no se pudo registrar");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila=tblProductos.getSelectedRow();
        if(fila==-1&&txtidProd.getText().isEmpty()){
            MenuPrincipal mp2=new MenuPrincipal();
            mp2.Advertencia("Debe seleccionar un registro de producto");
        }else{
            pd.setIdProducto(Integer.parseInt(txtidProd.getText()));
            pd.setNomProd(txtnombrePd.getText());
            pd.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
            pd.setPrecioVenta(Double.parseDouble(txtprecioVenta.getText()));
            if(daoPd.editar(pd)){
               MenuPrincipal mp3=new MenuPrincipal();
               mp3.Exito("Se modifico el registro de producto con éxito");
               limpiarCampos();
               limpiarTablaProducto();
               listarProducto();
            }else{
               MenuPrincipal mp4=new MenuPrincipal();
               mp4.Error("Error al modificar el registro de producto");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtidProd.getText().isEmpty()){
            int confirmar=JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar el registro de producto?","Confirmar",2);
            if(confirmar==0){
                pd.setIdProducto(Integer.parseInt(txtidProd.getText()));
                daoPd.eliminar(pd);
                limpiarCampos();
                limpiarTablaProducto();
                listarProducto();
                MenuPrincipal mp5=new MenuPrincipal();
                mp5.Exito("Se elimino el registro de producto con éxito");
            }
        }else{
            MenuPrincipal mp6=new MenuPrincipal();
            mp6.Advertencia("Debe seleccionar un registro de producto");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        pd.setIdProducto(Integer.parseInt(txtidProd.getText()));
        if(daoPd.buscar(pd)){
            txtidProd.setText(pd.getIdProducto()+"");
            txtnombrePd.setText(pd.getNomProd());
            txtidCategoria.setText(pd.getIdCategoria()+"");
            txtprecioVenta.setText(pd.getPrecioVenta()+"");
            txtstock.setText(pd.getStock()+"");
            ct.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
            if(daoCt.buscar(ct)){
                txtNomCategoria.setText(ct.getNomCategoria());
            }
        }  else {
            MenuPrincipal mp7=new MenuPrincipal();
            mp7.Error("El registro de producto no existe");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

