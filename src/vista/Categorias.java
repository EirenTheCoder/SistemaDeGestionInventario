    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        ct.setNomCategoria(txtnomCategoria.getText());
        if(daoCt.insertar(ct)){
            MenuPrincipal mp=new MenuPrincipal();
            mp.Exito("El registro de categoría se registro con éxito");
        } else {
            MenuPrincipal mp2=new MenuPrincipal();
            mp2.Error("El regitro de categoría no se pudo registrar");
        }
        limpiarTablaCategoria();
        listarCategorias();
        limpiarCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriasMouseClicked
        // TODO add your handling code here:
        int fila=tblCategorias.getSelectedRow();
        txtidCategoria.setText(tblCategorias.getValueAt(fila, 0).toString());
        txtnomCategoria.setText(tblCategorias.getValueAt(fila, 1).toString());
    }//GEN-LAST:event_tblCategoriasMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtidCategoria.getText().isEmpty()){
            int confirmar=JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar la categoría?","Confirmar",2);
            if(confirmar==0){
                ct.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
                daoCt.eliminar(ct);
                limpiarTablaCategoria();
                listarCategorias();
                limpiarCampos();
                MenuPrincipal mp3=new MenuPrincipal();
                mp3.Exito("El registro de categoría se elimino con éxito");
            }
        }else{
            MenuPrincipal mp4=new MenuPrincipal();
            mp4.Advertencia("Seleccione un registro de categoría");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ct.setIdCategoria(Integer.parseInt(txtidCategoria.getText()));
        if(daoCt.buscar(ct)){
            txtidCategoria.setText(ct.getIdCategoria()+"");
            txtnomCategoria.setText(ct.getNomCategoria());
        }else{
            MenuPrincipal mp5=new MenuPrincipal();
            mp5.Error("El registro de categoría no existe");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

