    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        p.setNombre(txtnombre.getText());
        p.setApellido(txtapellido.getText());
        p.setRuc(txtruc.getText());
        p.setRazonSocial(txtrazonSocial.getText());
        p.setDireccion(txtdireccion.getText());
        p.setTelefono(txttelefono.getText());
        p.setCorreo(txtcorreo.getText());
        if(dao.insertar(p)){
            MenuPrincipal mp=new MenuPrincipal();
            mp.Exito("El registro de proveedor se registro con éxito");
            limpiarCampos();
            limpiarTablaProveedor();
            listarProveedores();
        } else {
            MenuPrincipal mp1=new MenuPrincipal();
            mp1.Error("El regitro de proveedor no se pudo registrar");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila=tablaProveedores.getSelectedRow();
        if(fila==-1&&txtid.getText().isEmpty()){
            MenuPrincipal mp2=new MenuPrincipal();
            mp2.Advertencia("Debe seleccionar un registro de proveedor");
        }else{
            p.setIdProveedor(Integer.parseInt(txtid.getText()));
            p.setNombre(txtnombre.getText());
            p.setApellido(txtapellido.getText());
            p.setRuc(txtruc.getText());
            p.setRazonSocial(txtrazonSocial.getText());
            p.setDireccion(txtdireccion.getText());
            p.setTelefono(txttelefono.getText());
            p.setCorreo(txtcorreo.getText());
            if(dao.editar(p)){
               MenuPrincipal mp3=new MenuPrincipal();
               mp3.Exito("Se modifico el registro de proveedor con éxito");
               limpiarCampos();
               limpiarTablaProveedor();
               listarProveedores();
            }else{
               MenuPrincipal mp4=new MenuPrincipal();
               mp4.Error("Error al modificar el registro de proveedor");
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtid.getText().isEmpty()){
            int confirmar=JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar el proveedor?","Confirmar",2);
            if(confirmar==0){
                p.setIdProveedor(Integer.parseInt(txtid.getText()));
                dao.eliminar(p);
                limpiarCampos();
                limpiarTablaProveedor();
                listarProveedores();
                MenuPrincipal mp5=new MenuPrincipal();
                mp5.Exito("Se elimino el registro de proveedor con éxito");
            }
        }else{
            MenuPrincipal mp6=new MenuPrincipal();
            mp6.Advertencia("Debe seleccionar un registro de proveedor");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        p.setRuc(txtruc.getText());
        if(dao.buscarRuc(p)){
            txtid.setText(p.getIdProveedor()+"");
            txtnombre.setText(p.getNombre());
            txtapellido.setText(p.getApellido());
            txtruc.setText(p.getRuc());
            txtrazonSocial.setText(p.getRazonSocial());
            txtdireccion.setText(p.getDireccion());
            txttelefono.setText(p.getTelefono());
            txtcorreo.setText(p.getCorreo());
        }else{
            MenuPrincipal mp7=new MenuPrincipal();
            mp7.Error("El registro de proveedor no existe");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    void limpiarCampos(){
        txtid.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtruc.setText("");
        txtrazonSocial.setText("");
        txtdireccion.setText("");
        txttelefono.setText("");
        txtcorreo.setText("");
    }
    
    void limpiarTablaProveedor(){
        for(int i=0;i<modeloProveedor.getRowCount();i++){
            modeloProveedor.removeRow(i);
            i=0-1;
        }
    }
    
