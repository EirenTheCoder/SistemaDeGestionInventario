    private void listarProveedores(){
        List<Proveedor> lista=dao.Listar();
        modeloProveedor=(DefaultTableModel) tablaProveedores.getModel();
        Object[] ob=new Object[8];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getIdProveedor();
            ob[1]=lista.get(i).getNombre();
            ob[2]=lista.get(i).getApellido();
            ob[3]=lista.get(i).getRuc();
            ob[4]=lista.get(i).getRazonSocial();
            ob[5]=lista.get(i).getDireccion();
            ob[6]=lista.get(i).getTelefono();
            ob[7]=lista.get(i).getCorreo();
            modeloProveedor.addRow(ob);
        }
        tablaProveedores.setModel(modeloProveedor);
    }

